def load_subject_exercises_with_rep(subject_id="s1", base=BASE_SUBJECTS):
    folder = os.path.join(base, subject_id, f"DB3_{subject_id}")
    mat_files = sorted(glob.glob(os.path.join(folder, "*.mat")))

    emg_list, y_list, rep_list = [], [], []

    for fp in mat_files:
        m = loadmat(fp)
        emg_list.append(m["emg"].astype(np.float32))
        y_list.append(m["restimulus"].reshape(-1).astype(np.int32))
        rep_list.append(m["rerepetition"].reshape(-1).astype(np.int32))

    X = np.vstack(emg_list)
    y = np.concatenate(y_list)
    rep = np.concatenate(rep_list)

    return X, y, rep

