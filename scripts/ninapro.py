# STEP 5: Window subject s1

def window_emg_by_repetition(
    X, y, rep,
    fs=2000,
    win_ms=150,
    overlap=0.5
):
    win_len = int(fs * win_ms / 1000)      # 300 samples
    step = int(win_len * (1 - overlap))    # 150 step

    Xw, yw = [], []

    for r in np.unique(rep):
        idx = np.where(rep == r)[0]
        if len(idx) < win_len:
            continue

        Xr = X[idx]
        yr = y[idx]

        for start in range(0, len(Xr) - win_len + 1, step):
            end = start + win_len
            x_win = Xr[start:end].T               # (12, 300)
            label = yr[start + win_len // 2]      # center label

            Xw.append(x_win.astype(np.float32))
            yw.append(int(label))

    return np.stack(Xw), np.array(yw, dtype=np.int32)

